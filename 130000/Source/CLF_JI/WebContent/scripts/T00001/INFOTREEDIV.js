/**************************** 嵌入页面的形式打开数据 ***********************************/
function getDivInfoData(actionName,infoID,obj,rdoType){
	$(obj).html('');
	$.ajax({
		  type:'GET',
		  cache: false,
		  url:actionName,
		  data:{INFOID:infoID, RDOTYPE:rdoType},
		  complete:function()                                                    
          {
			
			$("#loading").hide();
		
			$($(obj).find(".expanded")).click(
			 function(){
				 var aINFOID = $(this).attr("expINFOID");
				 getDivInfoData(actionName,aINFOID,obj,rdoType);						 
			 	}
			 );
			 //给列表中的单选按钮添加单击事件
			 
			 //判断函数是否存在
			 if(typeof(getRdoValue)=="function") {
				 
				 $($(obj).find(".radio" + rdoType)).click(
					 function(){			
						 getRdoValue($(this).val(),rdoType,$(this).attr("expINFONM"));
					 }
				 );
				 
				 var objChk = $(obj).find(".radio" + rdoType + ":checked");
				 if ($(objChk).length==1)
				 {
					 getRdoValue($(objChk).val(),rdoType,$(objChk).attr("expINFONM"));
				 }
			 };
          },
		  success:function(res){
			 $(obj).html(res);			
		  },
		  error:function(){
			 alert('操作失败!');
		  }
	});
}

