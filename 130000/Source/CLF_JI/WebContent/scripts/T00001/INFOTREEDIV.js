/************************** 输入编码，显示导航 ******************************/
function getPath(actionName,infoID,objID,type){
	$.ajax({
		  type:'GET',
		  url: actionName,
		  cache: false,
		  data: {INFOID:infoID},
		  complete:function()                                                    
          {			
			$("#loading").hide();
			$("#txt"+type).val(infoID);
			 //判断函数是否存在
			 //if(typeof(getObjValue)=="function") {
			//	 getObjValue(infoID,type,objID);
			 //};
          },
		  success:function(res){
			 $(objID).val(res);
			 if(res==""){
				 var ctrlNm = $(objID).attr('id');
				 var ctrlId = ctrlNm.substr(0,ctrlNm.length-2)+"Id";
				 $("#"+ctrlId).val("");
				 
				 $("#hidSelect").val(type);
				 if(type == "ZJLX")
					openZJLXDialog('', '#infoTreeDIV');
				 if(type == "FWLX")
					openFWLXDialog('', '#infoTreeDIV');
				 if(type == "JYLX")
					openJYLXDialog('', '#infoTreeDIV');
				 if(type == "JZJGT")
					openJZJGDialog('', '#infoTreeDIV');
			 }
		  },
		  error:function(){
			 alert('操作失败!');
		  }
	});
}

